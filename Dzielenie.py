start = int (input ("Od jakiej liczby zacząć?: "))
koniec = int (input ("Na jakiej liczbie skończyć?: "))
dzielenie = int (input ("Przez jaką liczbę dzielić?: "))
number = start
while number <= koniec:
    if number % dzielenie == 0:
        print (number, "dzieli się przez", dzielenie)
    number += 1

